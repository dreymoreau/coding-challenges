// Very simple, given an integer or a floating-point number, find its opposite.

// depending on the inputted number, return its opposite
// return either -number or +number
function opposite(number) {
    return -number
    return +number
  }
  
  console.log(opposite(3.33)) // output => -3.33
  console.log(opposite(-8.88)) // output => 8.88